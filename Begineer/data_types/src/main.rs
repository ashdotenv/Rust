fn main() {
    // Boolean
    let a = true;
    let b = false;
    // unsigned Intergers #can't be negative
    let x: u32 = 50;
    let y: u8 = 50;
    // signed Intergers #can be negative
    let z: i32 = -40;
    // arrays
    let mut a: [i32; 5] = [1, 2, 4, 5, 6];
    a[0] = 5;
    //type aliasing
    type age = i32;
    let m: age = 50;
}
